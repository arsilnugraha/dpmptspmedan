                                    <p>Memulai berinvestasi/usaha<br>
                                    Dalam rangka untuk memulai usaha setiap pelaku usaha wajib memiliki Perizinan Berusaha, salah satu upaya pemerintah dalam meningkatkan kemudahan berusaha melalui perbaikan dan percepatan pelaksanaan berusaha dengan mengintegrasikan seluruh sistem yang terkait pelayanan perizinan dalam sistem Online Single Submission (OSS). OSS dibentuk pada tahun 2018 merupakan sistem terintegrasi dan menjadi gerbang (gateway) dari sistem pelayanan pemerintah yang telah ada di kementerian/lembaga dan pemerintah daerah dan saat ini telah dirubah dengan Perizinan Berusaha Berbasis Risiko. Dengan adanya OSS diharapkan perizinan untuk memulai investasi menjadi lebih sederhana, cepat dan mudah.<br>
                                    Berikut prosedur investasi di Indonesia dengan menggunakan OSS sehingga cara berinvestasi di Indonesia pun semakin praktis dan mudah melalui laman https://oss.go.id/.<br>
                                    Registrasi Akun OSS<br>
                                    Untuk memiliki akun OSS pelaku usaha baik perseorangan maupun non perseorangan harus memiliki nomor identitas yang sah (NIK bagi WNI dan Paspor bagi WNA), memiliki Nomor Pengesahan atau dasar hukum pembentukan badan usaha, dan Badan Usaha sudah mendapat NPWP. Setelah memasukkan data yang dibutuhkan, maka calon investor akan mendapatkan email yang berfungsi untuk aktivasi akun OSS tersebut dan akan mendapatkan user id dan password.<br>
                                    Pembuatan Perizinan Berusaha<br>
                                    Setelah membuat akun OSS maka calon investor harus membuat Nomor Induk Berusaha (NIB) sebagai identitas pelaku usaha dalam melakukan berbagai macam kegiatan usaha dan berlaku selama menjalankan kegiatan usaha. Selain sebagai bukti pendaftaran Penanaman Modal, NIB sekaligus merupakan legalitas dan API serta Hak Akses Kepabeanan (bersifat opsional, hanya bagi yang memerlukan). Ada beberapa data yang diperlukan untuk pembuatan NIB antara lain; data perusahaan, data akta, data pengurus dan pemegang saham, data kegiatan usaha (penentuan usaha memperhatikan ketentuan Bidang Usaha Penanaman Modal dan KBLI).</p>
