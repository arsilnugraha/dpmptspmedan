                                </div>
                                <div class="accordion-content">
                                    <p>Waktu pemeriksaan pemenuhan komitmen Izin Operasional/Komersial sesuai dengan ketentuan yang berlaku:</p>
                                    <ul style="padding-left: 20px;">
                                        <li>GMP (CPOB, CPBBAOB, CPOTB, CDOB, CPAKB, CPKB, CPPOB) : 35 hari</li>
                                        <li>Izin Edar (Obat, makanan, kosmetik, alat kesehatan) : 35 hari</li>
                                        <li>SNI (tipe 5) : 26 hari kerja</li>
                                        <li>SNI (tipe 1) : 7 hari kerja</li>
                                        <li>Pendaftaran Produk (NRP & NPB) : 3 hari kerja</li>
                                        <li>Lisensi personel (dokter) : 7 hari kerja</li>
                                        <li>Lisensi personel (apoteker) : 20 hari kerja</li>
                                        <li>Lisensi personel (perawat) : 3 hari kerja</li>
                                    </ul>
                                </div>
