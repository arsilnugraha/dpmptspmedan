                                    <p>Kota Medan adalah ibu kota provinsi Sumatera Utara Indonesia. Medan merupakan kota terbesar ketiga setelah DKI Jakarta dan Surabaya, serta kota terbesar di pulau Sumatera. Kota medan merupakan pintu gerbang wilayah Indonesia bagian barat dengan pelabuhan belawan dan Bandar Udara Internasional Kualanamu. Akses dari pusat kota dan bandara telah terlengkapi oleh jalan tol dan kereta api. Medan adalah kota pertama di Indonesia yang berhasil mengintegrasikan bandara dengan kereta.<br>
