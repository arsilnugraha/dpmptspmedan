                                    <div class="accordion-title"> Apa yang dimaksud dengan Izin Operasional ? </div>
                                </div>
                                <div class="accordion-content">
                                    <p>Izin Operasional/Komersial adalah izin yang diperlukan ketika kegiatan usaha memasuki tahapan komersial atau operasional. Izin Komersial atau Operasional yang diterbitkan OSS pada dasarnya merupakan komitmen atas pemenuhan berbagai izin/non-izin yang diperlukan pada tahapan komersial atau operasional, yaitu setelah pelaku usaha memperoleh izin usaha.</p>
