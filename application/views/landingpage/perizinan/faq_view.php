                                    <div class="accordion-title"> Apa bentuk dari Izin Operasional/Komersial ? </div>
                                </div>
                                <div class="accordion-content">
                                    <p>Bentuk izin komersial atau operasional ini antara lain: standar, sertifikat, izin ekspor/impor, persetujuan ekspor/impor. lisensi; dan/atau pendaftaran barang/jasa (izin/non-izin).</p>
                                </div>
                                <div class="accordion-header">
                                    <div class="accordion-icon">
                                        <i class="accordion-closed icon-ok-circle"></i>
                                        <i class="accordion-open icon-remove-circle"></i>
                                    </div>
                                    <div class="accordion-title"> Bagaimana langkah – langkah untuk mendapatkan Izin Operasional/Komersial ? </div>
                                </div>
                                <div class="accordion-content">
                                    <ul style="padding-left: 20px;">
                                        <li>
                                            Pelaku usaha mengisi pernyataan komitmen untuk menyelesaikan Izin Operasional/Komersial dalam jangka waktu tertentu. Pernyataan komitmen berupa kesanggupan untuk memenuhi:
                                            <ul style="padding-left: 40px;">
                                                <li>standar, sertifikat, dan/atau lisensi;</li>
                                                <li>pendaftaran barang/jasa; dan/atau</li>
                                                <li>pendaftaran kepabeanan dan perpajakan,</li>
                                            </ul>
                                            sesuai dengan jenis produk dan/atau jasa yang dikomersialkan oleh Pelaku Usaha
                                        </li>
                                        <li>Izin Operasional/Komersial diterbitkan otomatis setelah pernyataan komitmen diisi namun akan berlaku efektif setelah komitmen dipenuhi</li>
                                        <li>Pelaku usaha melaksanakan pemenuhan terhadap standar-standar atau persyaratan operasional/komersial, misalnya Cara Pembuatan Obat yang Baik, SNI dan sebagainya</li>
                                        <li>Pelaku usaha mengunggah dokumen-dokumen bukti pemenuhan terhadap standar yang ditentukan</li>
                                        <li>Pelaku usaha melengkapi atau memperbaiki dokumen (jika diminta oleh K/L/D)</li>
                                        <li>Pelaku usaha menjalani pemeriksaan fisik bangunan dan fasilitas (jika disyaratkan)</li>
                                        <li>Pelaku usaha menerima notifikasi dari OSS apakah komitmen penyelesaian Izin Operasional/Komersial dinyatakan diterima atau ditolak</li>
                                    </ul>
                                </div>
                                <div class="accordion-header">
