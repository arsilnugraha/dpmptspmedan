                                    <p>Geografis<br>
                                    Kota Medan memiliki luas 26.510 Hektar atau 3,6% dari keseluruhan wilayah Sumatera Utara. Dengan demikian, medan memiliki wilayah yang relatif kecil dengan jumlah penduduk yang relatif besar.</p>
